export function toggleChip(selected, optionKey, max) {
  const cur = Array.isArray(selected) ? [...selected] : [];
  const idx = cur.indexOf(optionKey);
  if (idx >= 0) {
    cur.splice(idx, 1);
    return cur;
  }
  if (max === 1) return [optionKey];
  if (cur.length >= max) return cur;
  cur.push(optionKey);
  return cur;
}

