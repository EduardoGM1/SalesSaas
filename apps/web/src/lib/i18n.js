export function t(key, lang = "es", vars) {
  const table = lang === "en" ? EN : ES;
  let text = table[key] ?? ES[key] ?? key;
  if (vars) {
    for (const [name, value] of Object.entries(vars)) {
      text = text.replaceAll(`{${name}}`, String(value));
    }
  }
  return text;
}

export function getLang(settings) {
  return settings?.language === "en" ? "en" : "es";
}

export function getMonths(lang = "es") {
  return lang === "en" ? MONTHS_EN : MONTHS_ES;
}

export function getWeekdays(lang = "es") {
  return lang === "en" ? WEEKDAYS_EN : WEEKDAYS_ES;
}

export function getWeekdaysShort(lang = "es") {
  return lang === "en" ? WEEKDAYS_SHORT_EN : WEEKDAYS_SHORT_ES;
}

export function statusLabelKey(status) {
  return STATUS_KEYS[status || ""] ?? null;
}

export function navLabel(label, lang = "es") {
  const map = {
    Agenda: "nav.agenda",
    Dashboard: "nav.dashboard",
    Metas: "nav.goals",
    Clientes: "nav.clients",
    Ventas: "nav.sales",
    Herramientas: "nav.tools",
    Red: "nav.network",
    Mensajes: "nav.messages",
    Admin: "nav.admin",
  };
  const k = map[label];
  return k ? t(k, lang) : label;
}

/** Traducción desde settings del store (acciones, utilidades fuera de React). */
export function translate(key, vars) {
  const lang = getLang(useDbStore.getState().db.settings);
  return t(key, lang, vars);
}
